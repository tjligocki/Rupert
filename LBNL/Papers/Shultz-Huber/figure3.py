def rotate(iv,t,i1,i2):
  ov = []
  for ip in iv:
    op = copy.copy(ip)
    op[i1] = cos(t)*ip[i1] - sin(t)*ip[i2]
    op[i2] = sin(t)*ip[i1] + cos(t)*ip[i2]
    ov.append(op)

  return ov

